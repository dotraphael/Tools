	Function GetRegistryKey
	{
		PARAM(
	        $RegPath,
	        $KeyName,
			$InvalidReturn
	    )
		
		if (!(Test-Path $RegPath))
		{
			return $InvalidReturn
		}
		else
		{
			$registry = (Get-ItemProperty -Path $RegPath).$KeyName
			if ($registry -eq $null) { return $InvalidReturn } else { return $registry }
		}
	}
	
	
	
